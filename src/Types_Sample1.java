    //코드의 약속된 시작점입니다.
    //명칭은 꼭 main이어야하고, Main, mains 등 안됩니다.
    public static void main(String[] args){
        //자료형
        //자료형을 왜쓰냐면
        // 1. 데이터의 형식을 표현할 수 있어요(약속)
        // 2. 메모리의 효율적인 사용 하기 위해서
        // 3. 팀 단위에서 개발 시 남들이 쉽게 알아 볼 수 있죠

        //메모리(RAM)에 데이터를 올리는 과정
        String name ="석재현"; //문자에 대한 자료형
        int age=10; //숫자에 대한 자료형
        boolean isMan=true; //True(참), False(거짓)이냐에 대한 자료형

        //메모리에 올라간 데이터를 출력
        System.out.println(name);
        System.out.println(age);
        System.out.println(isMan);
    }


